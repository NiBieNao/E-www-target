package com.jk.model;

public class Benci {

	
	 private String BenciId;
	
	 private String BenciName;
	 	
	 private int BenciType;

	 private String BenciDate;

	public String getBenciId() {
		return BenciId;
	}

	public void setBenciId(String benciId) {
		BenciId = benciId;
	}

	public String getBenciName() {
		return BenciName;
	}

	public void setBenciName(String benciName) {
		BenciName = benciName;
	}

	public int getBenciType() {
		return BenciType;
	}

	public void setBenciType(int benciType) {
		BenciType = benciType;
	}

	public String getBenciDate() {
		return BenciDate;
	}

	public void setBenciDate(String benciDate) {
		BenciDate = benciDate;
	}
	 
	 
}
